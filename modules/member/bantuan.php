  <center  style="font-family:GreyscaleBasic;font-weight:bold;"> <h3><b> PETUNJUK PENGGUNAAN SISTEM </b><h3> </center>

    <p align="justify">Bagi anda yang baru menggunakan Sistem Pakar ini, terdapat beberapa menu yang dapat anda pilih.
    </p>
    <p align="justify">Menu Tersebut di antaranya : </p>
    <p align="justify">
    1. Jenis Penyakit Bumil : Menu ini berisikan tentang penyakit bumil dan keteranganya <br>
    2. Register  : Menu ini berisika form untuk melakukan registrasi jika anda ingin melakukan diagnosa penyakit bumil. <br>
    3. Login     : Bagi anda yang sudah melakukan Registrasi , anda dapat melakukan login di halaman ini untuk selanjutnya melakukan diagnosa.<br>
    4. diagnosa: Menu ini adalah menu yang anda gunakan jika anda ingin melakukan diagnosa penyakit bumil, di dalam menu ini nantinya anda harus mengisi pertanyaan yang ada, caranya adalah dengan menceklis pertanyaan yang anda rasa sesuai dengan kondisi gejala anda saat ini alami, selanjutnya anda harus mengisikan  angka dari 0 - 1 berdasarkan seberapa besar gejala anda.<br>
    5. Kirim Pesan: Dengan menu ini anda dapat mengirimkan pesan kepada administrator jika ada yang ingin ditanyakan seputar sistem ini. <br>
    6. Ubah profil: Pada menu dropdown di kanan atas anda, anda dapat mengubah data diri anda. <br>
    7. Hasil Diagnosa: Pada menu ini anda dapat melihat riwayat diagnosa anda selama ini.<br>
    8. Inbox       : Pada menu ini anda dapat melihat balasan pesan dari administrator.<br>
    9. Logout      : Menu ini digunakan untuk keluar dari sistem.</p>
